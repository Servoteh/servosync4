function probniTrajanjeCyr(n: number | undefined): string {
  const m = Math.min(6, Math.max(1, parseInt(String(n), 10) || 6));
  const unit = m === 1 ? 'месец' : m <= 4 ? 'месеца' : 'месеци';
  return `${m} (${PROBNI_MESEC_RECI[m]}) ${unit}`;
}

